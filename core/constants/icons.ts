export type IconName = (typeof ALL_ICON_TYPES)[number];
export type LoadingIconName = (typeof LOADING_ICON_TYPES)[number];

// 型ガード関数
export const isLoadingIcon = (iconName: string): boolean =>
  iconName.startsWith('loading-') || iconName.startsWith('spinner');

export const isRegularIcon = (iconName: string): boolean =>
  !iconName.startsWith('loading-') && !iconName.startsWith('spinner');
